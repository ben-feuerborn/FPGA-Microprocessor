    jal SAD                 # jump to SAD function
    lw $ra, 0($sp)          # load return address from the stack
    addi $sp, $sp, 4        # deallocate space on the stack

    addi $sp, $sp, -4       # allocate space on stack for return address
    sw $ra, 0($sp)          # store return address on the stack
    jal min_SAD             # jump to min_SAD function
    lw $ra, 0($sp)          # load return address from the stack
    addi $sp, $sp, 4        # deallocate space on the stack

    addi $s0, $s0, -1       # move left one after iteration (curr_x -= 1)
    j left

# after hitting left border
left_exit:
    addi $s0, $s0, 1        # account for last loop iteration (curr_x += 1)
    addi $s1, $s1, -1       # move position up one (curr_y -= 1)
    addi $t1, $t1, -1       # move bottom border up one (bottom -= 1)


# moving up in circular search
up:
    slt $t8, $s1, $t0       # if top < curr_y, $t8 will be 0, exit up loop
    bne $t8, $zero, up_exit

    addi $sp, $sp, -4       # allocate space on stack for return address
    sw $ra, 0($sp)          # store return address on the stack
    jal SAD                 # jump to SAD function
    lw $ra, 0($sp)          # load return address from the stack
    addi $sp, $sp, 4        # deallocate space on the stack
    
    addi $sp, $sp, -4       # allocate space on stack for return address
    sw $ra, 0($sp)          # store return address on the stack
    jal min_SAD             # jump to min_SAD function
    lw $ra, 0($sp)          # load return address from the stack
    addi $sp, $sp, 4        # deallocate space on the stack

    addi $s1, $s1, -1       # move up one after iteration (curr_y -= 1)
    j up

# after hitting top border
up_exit:
    addi $s1, $s1, 1        # account for last loop iteration (curr_y += 1)
    addi $s0, $s0, 1        # move position right one (curr_x += 1)
    addi $t2, $t2, 1        # move left border right one (left += 1)


# check circular search routine conditions
circular_search:
    slt $t8, $t3, $t2       # if right < left, $t8 will be 1
    slt $t9, $t1, $t0       # if bottom < top, $t9 will be 1
    and $t8, $t8, $t9       # if both conditions are true, $t8 will be 1, exit circular search
    beq $t8, $zero, right

# exit search routine
exit_search:
    jr $ra



# SAD routine for any curr_x and curr_y position
SAD:
    li $t7, 0               # curr_SAD = 0
    li $t6, 0               # difference = 0
    li $t4, 0               # i = 0

i_loop:
    slt $t8, $t4, $s5       # check if i < window_height, if not exit SAD
    beq $t8, $zero, exit_SAD
    li $t5, 0               # j = 0

j_loop:
    # find difference:
    # $t6 will be difference
    # $t8 will be the index of a1, $t9 will be the index of a2
    add $t8, $s1, $t4       # index_1 = curr_y + i
    mul $t8, $t8, $s4       # index_1 = (curr_y + i) * frame_width
    add $t8, $t8, $s0       # index_1 = (curr_y + i) * frame_width + curr_x
    add $t8, $t8, $t5       # index_1 = (curr_y + i) * frame_width + (curr_x + j)
    sll $t8, $t8, 2         # multiply index_1 by 4 for offset
    add $t8, $a1, $t8       # $t8 = a1[index_1]
    lw $t8, 0($t8)

    mul $t9, $t4, $s6       # index_2 = i * window_width
    add $t9, $t9, $t5       # index_2 = (i * window_width) + j
    sll $t9, $t9, 2         # multiply index_2 by 4 for offset
    add $t9, $a2, $t9       # $t9 = a2[index_2]
    lw $t9, 0($t9)

    sub $t6, $t8, $t9       # difference = a1[index_1] - a2[index_2]

# finds absolute value when < 0
ABS_VAL:
    bgez $t6, skip_ABS      # will skip ABS_VAL function when >= 0
    sub $t6, $zero, $t6     # finds absolute value of $t6 (difference)

skip_ABS:
    # once ABS_VAL of difference found, add to curr_SAD
    add $t7, $t7, $t6       # curr_SAD += difference

    # iterate through loop
    addi $t5, $t5, 1        # j++
    slt $t8, $t5, $s6       # check if j < window_width, if true iterate
    bne $t8, $zero, j_loop
    addi $t4, $t4, 1        # else i++ and jump to i_loop
    j i_loop
exit_SAD:
    jr $ra                  # return to current place within circular_search


# updates min_SAD, if curr_SAD < min_SAD
min_SAD:
    slt $t8, $t7, $s7       # check if curr_SAD < min_SAD
    beq $t8, $zero, exit_min_SAD
    move $s7, $t7           # updates min_SAD (min_SAD = curr_SAD)
    move $v0, $s1           # updates min_SAD x-coordinate (min_SAD_x = curr_x) (row)
    move $v1, $s0           # updates min_SAD y-coordinate (min_SAD_y = curr_y) (col)
exit_min_SAD:
    jr $ra                  # return to current place in j_loop